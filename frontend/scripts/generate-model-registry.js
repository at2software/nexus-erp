/**
 * Auto-generate model registry from src/models directory
 * Run with: node scripts/generate-model-registry.js
 */

const fs = require('fs');
const path = require('path');

const MODELS_DIR = path.join(__dirname, '../src/models');
const OUTPUT_FILE = path.join(__dirname, '../src/constants/model-registry.ts');

/**
 * Recursively find all .model.ts files
 */
function findModelFiles(dir, fileList = []) {
    // Validate and normalize directory path
    const normalizedDir = path.normalize(dir);
    if (!normalizedDir.startsWith(MODELS_DIR)) {
        console.warn(`Invalid directory path: ${dir}`);
        return fileList;
    }

    // nosemgrep: eslint.detect-non-literal-fs-filename - path validated above
    const files = fs.readdirSync(normalizedDir);
    files.forEach(file => {
        const filePath = path.normalize(path.join(normalizedDir, file));
        // Ensure filePath is within MODELS_DIR
        if (!filePath.startsWith(MODELS_DIR)) {
            console.warn(`Invalid path detected and skipped: ${filePath}`);
            return;
        }
        // nosemgrep: eslint.detect-non-literal-fs-filename - path validated above
        const stat = fs.statSync(filePath);
        if (stat.isDirectory()) {
            findModelFiles(filePath, fileList);
        } else if (file.endsWith('.model.ts')) {
            fileList.push(filePath);
        }
    });
    return fileList;
}

/**
 * Extract exported class names from a TypeScript file
 */
function extractClassNames(filePath) {
    // Validate and normalize file path
    const normalizedPath = path.normalize(filePath);
    if (!normalizedPath.startsWith(MODELS_DIR)) {
        console.warn(`Invalid file path: ${filePath}`);
        return [];
    }

    // nosemgrep: eslint.detect-non-literal-fs-filename - path validated above
    const content = fs.readFileSync(normalizedPath, 'utf-8');
    const classMatches = content.matchAll(/export\s+class\s+(\w+)\s+extends\s+(?:Serializable|VcardClass)/g);

    const classNames = [];
    for (const match of classMatches) {
        classNames.push(match[1]);
    }
    return classNames;
}

/**
 * Convert absolute path to @models alias path
 */
function toModulePath(filePath) {
    const relativePath = path.relative(MODELS_DIR, filePath);
    const normalizedPath = relativePath.replace(/\\/g, '/').replace(/\.ts$/, '');
    return `@models/${normalizedPath}`;
}

/**
 * Generate the model registry
 */
function generateRegistry() {
    const modelFiles = findModelFiles(MODELS_DIR);
    const registry = {};
    
    modelFiles.forEach(filePath => {
        const classNames = extractClassNames(filePath);
        const modulePath = toModulePath(filePath);
        
        classNames.forEach(className => {
            registry[className] = modulePath;
        });
    });
    
    // Sort by class name for consistency
    const sortedRegistry = Object.keys(registry)
        .sort()
        .reduce((obj, key) => {
            obj[key] = registry[key];
            return obj;
        }, {});
    return sortedRegistry;
}

/**
 * Write the registry file
 */
function writeRegistryFile(registry) {
    // Validate output path
    const normalizedOutput = path.normalize(OUTPUT_FILE);
    const expectedDir = path.join(__dirname, '../src/constants');
    if (!normalizedOutput.startsWith(expectedDir)) {
        throw new Error(`Invalid output path: ${OUTPUT_FILE}`);
    }

    // Generate import statements
    const imports = Object.entries(registry)
        .map(([className, modulePath]) => `import { ${className} } from '${modulePath}';`)
        .join('\n');

    // Generate registry entries
    const entries = Object.keys(registry)
        .map(className => `    '${className}': ${className},`)
        .join('\n');

    const content = `/**
 * Model Registry - Auto-generated model path mappings
 *
 * This file is AUTO-GENERATED by scripts/generate-model-registry.js
 * DO NOT EDIT MANUALLY - Run 'npm run generate:models' to regenerate
 *
 * This file maps model class names to their class constructors for dynamic instantiation.
 */

${imports}

export const MODEL_REGISTRY: Record<string, any> = {
${entries}
};
`;

    // nosemgrep: eslint.detect-non-literal-fs-filename - path validated above
    fs.writeFileSync(normalizedOutput, content, 'utf-8');
    console.log(`✓ Generated model registry with ${Object.keys(registry).length} models`);
    console.log(`  Output: ${normalizedOutput}`);
}

// Execute
try {
    const registry = generateRegistry();
    writeRegistryFile(registry);
} catch (error) {
    console.error('✗ Failed to generate model registry:', error);
    process.exit(1);
}
